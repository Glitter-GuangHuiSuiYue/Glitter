package keyword.bean;

public class Location {
	private Long id;
	private String location;
	private String country;
	private String state;
	private String city;
	private String district;
	private String street;
	private Float lagtitute;
	private Float longtitute;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public Float getLagtitute() {
		return lagtitute;
	}
	public void setLagtitute(Float lagtitute) {
		this.lagtitute = lagtitute;
	}
	public Float getLongtitute() {
		return longtitute;
	}
	public void setLongtitute(Float longtitute) {
		this.longtitute = longtitute;
	}
	
}
